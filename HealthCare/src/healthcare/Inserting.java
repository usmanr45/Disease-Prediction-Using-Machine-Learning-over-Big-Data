    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      
       try
        {
        FileReader file = new FileReader(cpath1);
            BufferedReader br = new BufferedReader(file);
            String read = null;
            while ((read = br.readLine()) != null) {
                StringTokenizer str = new StringTokenizer(read, ",");
                while (str.hasMoreTokens()) {

                    System.out.println("\n Success");
                   
                    String query = "INSERT INTO data2 VALUES ('" + str.nextToken() + "','" + str.nextToken() + "','" + str.nextToken() + "','" + str.nextToken() + "','" + str.nextToken() + "','" + str.nextToken() + "','" + str.nextToken() + "','" + str.nextToken() + "','" + str.nextToken() + "','" + str.nextToken() + "')";
                    st.executeUpdate(query);
                    
                }
            }
            br.close();
            file.close();
            JOptionPane.showMessageDialog(this,"Patient Report is Successfully inserted");
            System.out.println("\n Inserted Successfully");

        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

